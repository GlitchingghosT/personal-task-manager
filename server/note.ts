// BACKEND NOTE
// WHAT IS BACKEND?
// THIS REFERS TO THE PART OF A WEBSITE THAT OPERATES ON THE SERVER SIDE,  BEHIND THE SCENES.
// RESPONSIBLE FOR MANAGING AND PROCESSING, HANDLING BUSINESS LOGIC AND PERFORMING TASK THE FRONTEND CANNOT HANDLE
// WHAT DO WE USE BACKEND TO DO?
// 1. SERVER: THE BACKEND RUNS ON A SERVER, WHICH CAN BE A SINGLE COMPUTER/LAPTOP, A CLUSTER OF COMPUTERS OR SUPER COMPUTERS WHICH HOUSES THE WEBSITE/WEBAPPS.
// 2. DATABASE: THE BACKEND INTERACTS WITH A DATABASE TO STORE AND RETRIEVE DATA. THIS INCLUDES USER INFORMATION, CONTENTS AND OTHER DATA NECESSARY FOR THE WEBSITE FUNCTIONALITY. (MONGODB).
// 3. SERVERSIDE PROGRAMMING LANGUAGES: JAVASCRIPT/TYPESCRIPT(NODE/{FRAMEWORK: EXPRESS}), PYTHON(DJANGO), PHP(LARAVEL).
// 4. API: (APPLICATION PROGRAMMING INTERFACE) THE BACKEND CAN EXPOSE APIS THAT THE FRONTEND USES TO REQUEST AND SEND DATA.
// 5. AUTHENTICATION AND AUTHORIZATION: THE BACKEND MANAGES USERS AUTHENTICATION (VERIFYING IDENTITY) AND AUTHORIZATION (GRANTING ACCESS TO RESOURCES BASED ON PERMISSION).
// SECURITY: THE BACKEND IMPLEMENTS SECURITY MEASURES TO PROTECT DATA AND ENSURES THAT THE APPLICATION IS SAFE FROM VULNERABILITIES.

// TOOLS USED FOR BACKEND DEVELOPMENT
// VS CODE (CODE EDITOR)
// DATABASE: POSTGRESS, MYSQL, MONGODB, NOSQL
// LANGUAGES: NODE... EXPRESS
// TESTERS: POSTMAN

// REQUEST AND RESPONSE
// REQUEST: 5 TYPES OF REQUEST
// 1. POST REQUEST: USER SENDING THERE INFORMATION TO A SERVER .E.G. LOGIN, SIGN-UP, INPUTS ETC
// 2. GET REQUEST: RETRIVES DATA FROM THE SERVER/DATABASE.
// 3. DELETE REQUEST: LOGOUT, DELETE ACCOUNT, DELETE A POST.
// 4. PATCH REQUEST: TO EDIT A DATA THAT HAS ALREADY BEEN POSTED.
// 5. PUT REQUEST: TO UPDATE A DATA THAT HAS ALREADY BEEN POSTED.

// REPONSE
// GOOD & BAD
// STATUS CODE

// BACKEND FLOW
// STEP 1: APP.TS, INDEX.TS, MAIN.TS: THIS IS WHERE YOU RUN SERVER AND CONNECT TO YOUR DATABASE.
// STEP 2: YOU CREATE A MODEL FOLDER: THIS IS FOR DEFENDING THE STRUCTURE OF THE DATA THAT WOULD BE STORED IN YOUR DATABASE.
// STEP 3: YOU CREATE THE CONTROLLER FOLDER: THIS IS WHERE THE BUSINESS LOGIC HAPPENS... WHERE YOU DEFINE WHAT HAPPENS BETWEEN REQUEST AND RESPONSE.
// STEP 4: YOU CREATE THE ROUTES FOLDER: THIS IS WHERE YOU DEFINE YOUR REQUEST ROUTE.
// STEP 5: YOU BRING EVERYTHING BACK THE SERVER FILE.
// STEP 6: OTHER FOLDER YOU MAY CREATE DEPENDING OF THE SCALE OF THE PRODUCT: 1. UTILS FOLDER (UTILITIES), MIDDLEWARE FOLDER E.T.C.

